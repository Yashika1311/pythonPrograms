class addition:
    def add(self,x,y):
        self.x=x
        self.y=y
        print(self.x+self.y)

class subtraction:
    def sub(self,x,y):
        self.x=x
        self.y=y
        print(self.x-self.y)


class division:
    def div(self,x,y):
        self.x=x
        self.y=y
        print(self.x/self.y)



   